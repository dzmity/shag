public class Calculator {


    public static int summ(int arg1, int arg2) {
        System.out.println("Суммируем инты");
        return arg1 + arg2;
    }

    public static long summ(long arg1, long arg2) {
        System.out.println("Суммируем лонги");
        return arg1 + arg2;
    }

    public static double summ(double arg1, double arg2) {
        System.out.println("Суммируем даблы");
        return arg1 + arg2;
    }
}
